package com.example;

/**
 * A tiny calculator used for CI/CD demo pipelines.
 */
public final class Calculator {

    public int add(int left, int right) {
        return left + right;
    }

    public int subtract(int left, int right) {
        return left - right;
    }

    public int multiply(int left, int right) {
        return left * right;
    }
}
